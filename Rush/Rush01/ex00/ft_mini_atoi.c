int ft_mini_atoi(char *str)
{
    int	i;
	int	num;

	i = 0;
	num = 0;

	while (str[i] && str[i] >= 48 && str[i] <= 57)
	{
		num = num * 10 + (str[i] - 48);
		i++;
	}
	return (num);
}