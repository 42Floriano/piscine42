int ft_check_array(int *array)
{
    int i;

    i = 0;

    while(i < 16)
    {
        if(array[i] < 1 || array[i]  > 4)
			{
			return (1);
			}
        i++;
    }
    if(i != 16)
        return (1);

    return (0);
}